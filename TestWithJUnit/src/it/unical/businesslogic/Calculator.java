package it.unical.businesslogic;

public class Calculator {

	public int multiply(int factor1, int factor2) {
		return factor1 * factor2;
	}
	
	public int add(int addendum1, int addendum2) {
		return addendum1 + addendum2;
	}
	
	public int division(int dividendum, int divisor) {
		return dividendum / divisor;
	}
}
